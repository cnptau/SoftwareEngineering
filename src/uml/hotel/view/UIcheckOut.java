	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
		// TODO add your handling code here:
		String room_id = jTextField1.getText();
		String checkType = jComboBox1.getSelectedItem().toString();

		if (checkType.equalsIgnoreCase("预约顾客结账")) {
			CheckInDAO findInfo = new CheckInDAO();//声明在入住表单中查找顾客信息的接口对象
			CheckIn c = new CheckIn();//声明一个接受表单一行的对象
			List<?> li = findInfo.findByRoomId(room_id);
			c = (CheckIn) li.get(0);
			Day = c.getLiveDate();
			setTextFiled(c.getBookingId(), c.getCustomerId(), c.getName(), c
					.getStartTime(), c.getEndTime(), Integer.toString(c
					.getLiveDate()));

		} else if (checkType.equalsIgnoreCase("散客结账")) {
			WalkInDAO findInfo = new WalkInDAO();//声明在入住表单中查找顾客信息的接口对象
			WalkIn c = new WalkIn();//声明一个接受表单一行的对象
			List<?> li = findInfo.findByRoomId(room_id);
			c = (WalkIn) li.get(0);
			Day = c.getLiveDate();
			setTextFiled(c.getBookingId(), c.getCustomerId(), c.getName(), c
					.getStartTime(), c.getEndTime(), Integer.toString(c
					.getLiveDate()));
		} else {
			System.out.println("input a wrong infomation");
		}

		RoomDAO fr = new RoomDAO();
		room_id = jTextField1.getText();
		List<?> lr = fr.findByRoomId(room_id);
		Room r = new Room();
		r = (Room) lr.get(0);
		roomcost = r.getPrice();
		roomcost = roomcost * Day;//住店期间房间费
		String cost = Integer.toString(roomcost);
		setroomcost(cost);
		setTextroom(Integer.toString(r.getPrice()), r.getDescription());

		//显示该结账房间增加消费表内容
		CostDAO co = new CostDAO();
		//Cost cIn=new Cost();
		List<?> cli = co.findByRoomId(room_id);
		settable(cli);
		//获得结账时应收金额
		int costtotal = getServerCost(cli);
		costtotal = costtotal + roomcost;
		jTextField10.setText(Integer.toString(costtotal));
		totalmoney = costtotal;//用于结账时计算找零
	}

