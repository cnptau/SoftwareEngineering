	public void settable(List<?> Item) {
		// TODO Auto-generated method stub
		int numItem = Item.size();
		Object o[][] = new Object[numItem][4];
		for (int i = 0; i < numItem; i++) {
			Cost sc = (Cost) ((java.util.List<?>) Item).get(i);
			o[i][0] = sc.getServiceName();
			o[i][1] = sc.getServicePrice();
			o[i][2] = sc.getNumb();
			o[i][3] = sc.getCost();
		}
		String[] coluNames = { "消费名称", "单价", "数量", "总价格" };
		jTable1.setModel(new javax.swing.table.DefaultTableModel(o, coluNames));
	}

	public int getServerCost(List<?> Item) {
		// TODO Auto-generated method stub
		int numItem = Item.size();
		int cost = 0;
		String o[][] = new String[numItem][1];
		for (int i = 0; i < numItem; i++) {
			Cost sc = (Cost) ((java.util.List<?>) Item).get(i);
			o[i][0] = sc.getCost();
			cost = cost + Integer.parseInt(o[i][0]);

		}
		return cost;
	}

	public void deleteInfo() {
		String roomId = jTextField1.getText();
		String Ctype = jComboBox1.getSelectedItem().toString();
		//从入住表中删除顾客信息
		if (Ctype.equalsIgnoreCase("预约顾客结账")) {
			CheckInDAO findInfo = new CheckInDAO();//声明在入住表单中查找顾客信息的接口对象
			CheckIn c = new CheckIn();//声明一个接受表单一行的对象
			List<?> li = findInfo.findByRoomId(roomId);
			c = (CheckIn) li.get(0);
			findInfo.delete(c);

		} else if (Ctype.equalsIgnoreCase("散客结账")) {
			WalkInDAO findInfo = new WalkInDAO();//声明在入住表单中查找顾客信息的接口对象
			WalkIn c = new WalkIn();//声明一个接受表单一行的对象
			List<?> li = findInfo.findByRoomId(roomId);
			c = (WalkIn) li.get(0);
			findInfo.delete(c);
		} else {
			System.out.println("input a wrong infomation");
		}
		//服务表中删除增加消费的信息
		CostDAO cost = new CostDAO();
		List<?> co = cost.findByRoomId(roomId);
		int n = co.size();
		for (int i = 0; i < n; i++) {
			Cost c = (Cost) co.get(i);
			cost.delete(c);
		}
	}
